# To get the length of a string, use the len() function.
str_example = "Hello, World!"
length = len(str_example)
print(f"Length of '{str_example}' is {length}")

# To check if a certain phrase or character is present in a string, we can use the keyword "in".
Name="Ashikur Rahaman Akash"
if "Ashik" in Name:
    print(f"Yes, 'Ashik' is present in the string '{Name}'.")


#To check if a certain phrase or character is NOT present in a string, we can use the keyword "not in".

if "Mehedi" not in Name:
    print(f"No, 'Mehedi' is NOT present in the string '{Name}'.")